def build_dataset() -> list[dict]:
    records = []
    idx = 1
    for domain in DOMAINS:
        for topic_name, topic_keywords in domain["topics"]:
            keywords = []
            for word in [*domain["base_keywords"], *topic_keywords]:
                if word not in keywords:
                    keywords.append(word)
            records.append(
                {
                    "id": f"sample_{idx:03d}",
                    "topic": f"{domain['domain']}：{topic_name}",
                    "language": "中文",
                    "style": "专业简洁",
                    "generateFromUploadedFile": False,
                    "generateFromWebSearch": True,
                    "expected_keywords": keywords,
                    "required_slide_types": ["cover", "contents", "transition", "content", "end"],
                    "max_words_per_page": DEFAULT_LIMITS,
                    "user_modification_rules": {
                        "forbidden_phrases": ["待补充", "TODO", "lorem ipsum", "示例文本"],
                        "keyword_hit_threshold": 0.5,
                    },
                }
            )
            idx += 1
    if len(records) != 100:
        raise ValueError(f"测试集数量异常，期望 100，实际 {len(records)}")
    return records


def main() -> None:
    root = Path(__file__).resolve().parent
    output_path = root / "auto_eval_dataset_100.json"
    dataset = build_dataset()
    output_path.write_text(json.dumps(dataset, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"已生成测试集：{output_path}")
    print(f"数据条数：{len(dataset)}")


if __name__ == "__main__":
    main()
